package giris;

public class karsilastirma_operatorleri {
    public static void main(String[] args) {
        int a = 5, b = 10;
        System.out.println(a < b);
        System.out.println(a == b); // bir = değer ataması yapar iki = eşit mi diye kontrol eder
        System.out.println(a != b); // != eşit değil anlamına gelir
        System.out.println(a > b);
        System.out.println(a <= b);
        System.out.println(a >= b);
    }
}
